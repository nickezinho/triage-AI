class AiAnalysesViewSet(viewsets.ModelViewSet):
    """
    AI analyses endpoint.

    Return AI-generated analyses for medical triages.

    This endpoint retrieves analyses generated by the Gemini AI
    after a triage is created.

    Query parameters:
    - triage (integer): ID of the triage used to filter analyses.

    If the triage paramter is provided, the endpoint returns only the analysis related to that triage.

    Example request:

    GET /analysis/?triage=7

    Example response:

    [
        {
            "id": 1,
            "prompt": "triagem médica",
            "resposta": {
                "risco": "moderado",
                "possiveis_causas": [
                    "Gripe (Influenza)",
                    "COVID-19",
                    "Resfriado comum",
                    "Pneumonia"
                ],
                "especialidades_recomendadas": [
                    "Clínico Geral",
                    "Infectologista"
                ],
                "recomendacoes": "Mantenha repouso e hidratação constante. Monitore a temperatura corporal. Recomenda-se buscar avaliação médica em uma unidade de pronto atendimento para diagnóstico diferencial, especialmente se houver falta de ar, dor no peito ou persistência da febre."
            },
            "model": "gemini-3-flash-preview",
            "created_at": "2026-03-10T11:55:39.167885-03:00",
            "triage": 6
        }
    ]
    
    """
    queryset = AIanalyses.objects.all()
    serializer_class = AIanalysesSerializer
    http_method_names = ['get']

    def get_queryset(self):
        """
        Filters analyses by triage ID
        """
        queryset = AIanalyses.objects.all()
        triage_id = self.request.query_params.get('triage')

        if triage_id:
            queryset = queryset.filter(triage_id=triage_id)
        
        return queryset

