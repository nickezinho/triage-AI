from rest_framework import viewsets
from .serializers import AIanalysesSerializer, TriageSerializer
from .models import AIanalyses, Triage
from .services.ai_service import GeminiService
from .services.maps_service import MapsService
from rest_framework.views import APIView
from rest_framework.response import Response
from .throttles import TriageRateThrottle


class TriageViewSet(viewsets.ModelViewSet):
    """
        API endpoint for medical triage.

        POST /triage/
        Creates a new medical triage based on patient symptoms.

        After the triage is created, the system automatically sends the data to the Gemini AI service 
        to generate a risk analysis.

        Example request:

        {
            "age": 19,
            "symptom1": "fever",
            "symptom2": "cough",
            "symptom3": "",
            "description":"pain when breathing",
            "duration": 2

        }
    """
    queryset = Triage.objects.all()
    serializer_class = TriageSerializer
    http_method_names = ['post','get'] # GET Temporary
    throttle_classes = [TriageRateThrottle]

    def perform_create(self, serializer):
        """
        Save triages data and trigger the AiAnalysis.
        """
 
        triage = serializer.save()
 
        service = GeminiService()
 
        resposta = service.analise(triage)
 
        AIanalyses.objects.create(
        triage=triage,
        prompt="triagem médica",
        resposta=resposta,
        model="gemini-3-flash-preview"
        )

        

class NearbyHospitalsView(APIView):
    """
        Nearby hospitals endpoint.

        Returns hospitals near the provided geographic coordinates.
        The hospitals are retrieved using the Google Maps API.

        Query parameters:
        - lat (float): Latitude of the user location
        - lng (float): Longitude of the user location
        - speciality (string): Medical speciality used to filter hospitals

        Example request:

        GET /hospitals/nearby/?lat=-XX.XXXX&lng=-XX.XXXX&speciality=cardiology

        Example response:

        [
            {
                "name": "Hospital Santa Cruz",
                "address": "Rua Santa Cruz, São Paulo",
                "maps_link": "https://maps.google.com/..."
            }
        ]
        """

    def get(self, request):
        """
        Retrieves nearby hospitals based on location and speciality.
        """
        lat = request.query_params.get("lat")
        lng = request.query_params.get("lng")
        speciality = request.query_params.get("speciality")

        service = MapsService()

        hospitals = service.search_hospital(lat, lng, speciality)

        return Response(hospitals)


